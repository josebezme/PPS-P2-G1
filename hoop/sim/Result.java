package hoop.sim;

public class Result {

	public final String teamA;
	public final String teamB;
	public final int scoreA;
	public final int scoreB;

	public Result(String teamA, String teamB, int scoreA, int scoreB)
	{
		this.teamA = teamA;
		this.teamB = teamB;
		this.scoreA = scoreA;
		this.scoreB = scoreB;
	}
}
